#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	if (fd < 0 || fd > 4096 || s == 0)
		return (0);
	while (*s)
	{
		if (ft_putchar_fd(*s, fd) == -1)
			return (-1);
		s++;
	}
	return (0);
}
